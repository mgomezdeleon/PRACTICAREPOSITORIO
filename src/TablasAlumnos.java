
public class TablasAlumnos {
			public static void main(String[] args) {
			System.out.println("Tablas de Multiplicar");
			for(int i=0; i<11;i++) {
				
				System.out.println("Tabla del " + i);
				for(int j=0;j<11;j++) {
					
					System.out.println(i + " x " + j + "= " + (i * j));
					
				}
				System.out.println("**************");
			}
		}
	}


